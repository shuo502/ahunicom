def imageResize(input_path_file, output_path_file):
    # 计算两个坐标的距离
    def distance_between_points(x1, y1, x2, y2):
        distance = math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)
        return distance

    file = input_path_file[input_path_file.rfind('/') + 1:]

