    if f_size <= 55000:  # 55kb
        return {'msg': 'err', 'data': f"图片{file}过小{int(f_size / 1024)}kb "}
    elif f_size < 1024 * 1024:  # 1024k
        return {'msg': 'ok', 'data': input_path_file}  # 1mb大小
