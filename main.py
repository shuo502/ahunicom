def data_to_srv(data,p=False,d=False,dictx='t'):

    vkey,u=[],{}
    def xx(data,vkey ,p,d,dictx):
        for key in data:
            if isinstance(data[key], dict):
                vkey.append(key)
                xx(data[key],vkey,p,d,dictx)
                vkey.pop()
            else:
                u[key] = data[key]
                s = ''.join([f'["{i}"]' for i in vkey]) + f'["{key}"]' if vkey else f'["{key}"]'
                if d: print(f"{dictx}{s}={ data[key]}")
                if p: print(f"{key}={dictx}{s}")
    xx(data,vkey,p,d,dictx)
    return u

